// JavaScript Naming Conventions
//
// camelCase:
// - Used for variables and functions
// - Example: userName, getTotal, isVisible
//
// PascalCase:
// - Used for classes, constructors, and React components
// - Example: Person, ShoppingCart, UserProfile
//
// snake_case:
// - Rare in JavaScript identifiers, but sometimes used for constants in older code or when matching external systems
// - Example: user_name, max_value
//
// UPPER_SNAKE_CASE:
// - Common for truly constant values
// - Example: MAX_RETRIES, API_URL, DEFAULT_TIMEOUT
//
// kebab-case:
// - Not a valid JavaScript identifier, but commonly used for filenames, CSS classes, and HTML attributes
// - Example: my-component.js, site-header
